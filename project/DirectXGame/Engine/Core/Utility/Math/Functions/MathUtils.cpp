// 基本的なベクトル演算

// ベクトルの加算
Vector3 Add(const Vector3& v1, const Vector3& v2) {
	Vector3 result;
	result.x = v1.x + v2.x;
	result.y = v1.y + v2.y;
	result.z = v1.z + v2.z;
	return result;
}

// ベクトルの減算
Vector3 Subtract(const Vector3& v1, const Vector3& v2) {
	Vector3 result;
	result.x = v1.x - v2.x;
	result.y = v1.y - v2.y;
	result.z = v1.z - v2.z;
	return result;
}

// ベクトルの積（要素ごとの積）
Vector3 Multiply(const Vector3& v1, const Vector3& v2) {
	Vector3 result;
	result.x = v1.x * v2.x;
	result.y = v1.y * v2.y;
	result.z = v1.z * v2.z;
	return result;
}

// 内積
float Dot(const Vector3& v1, const Vector3& v2) {
	float result;
	result = v1.x * v2.x + v1.y * v2.y + v1.z * v2.z;
	return result;
}

// クロス積
Vector3 Cross(const Vector3& v1, const Vector3& v2) {
	Vector3 result{};
	result.x = (v1.y * v2.z) - (v1.z * v2.y);
	result.y = (v1.z * v2.x) - (v1.x * v2.z);
	result.z = (v1.x * v2.y) - (v1.y * v2.x);
	return result;
}

// 長さ（ノルム）
float Length(const Vector3& v) {
	float result;
	result = sqrtf(v.x * v.x + v.y * v.y + v.z * v.z);
	return result;
}

// 正規化
Vector3 Normalize(const Vector3& v) {
	Vector3 result;
	float length = Length(v);
	if (length != 0) {
		result.x = v.x / length;
		result.y = v.y / length;
		result.z = v.z / length;
	} else {
		result.x = 0;
		result.y = 0;
		result.z = 0;
	}
	return result;
}

// 垂直なベクトルを求める関数
Vector3 Perpendicular(const Vector3& vector) {
	if (vector.x != 0.0f || vector.y != 0.0f) {
		return { -vector.y, vector.x, 0.0f };
	}
	return { 0.0f, -vector.z, vector.y };
}

// 反射ベクトルを求める関数
Vector3 Reflect(const Vector3& input, const Vector3& normal) {
	Vector3 normalizedNormal = Normalize(normal);
	float dotProduct = Dot(input, normalizedNormal);
	Vector3 result;
	result.x = input.x - 2 * dotProduct * normalizedNormal.x;
	result.y = input.y - 2 * dotProduct * normalizedNormal.y;
	result.z = input.z - 2 * dotProduct * normalizedNormal.z;
	return result;
}

// 球との衝突判定を行う関数
bool IsCollision(const Sphere& sphere, const Plane& plane) {
	// 平面の法線ベクトルを正規化
	Vector3 normal = Normalize(plane.normal);
	// 球の中心から平面までの距離を計算
	float distance = Dot(plane.normal, sphere.center) + plane.distance;
	// 球の半径と平面までの距離を比較
	return distance <= sphere.radius;
}

// ベクトルを法線方向に投影する関数
Vector3 Project(const Vector3& vector, const Vector3& normal) {
	// ベクトルを法線方向に投影する
	float dotProduct = Dot(vector, normal);
	return normal * dotProduct;
}

// 基本的な行列演算

// 4x4行列の加算
Matrix4x4 Add(const Matrix4x4& m1, const Matrix4x4& m2) {
	Matrix4x4 result;
	for (int i = 0; i < 4; i++) {
		for (int j = 0; j < 4; j++) {
			result.m[i][j] = m1.m[i][j] + m2.m[i][j];
		}
	}
	return result;
}

// 4x4行列の減算
Matrix4x4 Subtract(const Matrix4x4& m1, const Matrix4x4& m2) {
	Matrix4x4 result;
	for (int i = 0; i < 4; i++) {
		for (int j = 0; j < 4; j++) {
			result.m[i][j] = m1.m[i][j] - m2.m[i][j];
		}
	}
	return result;
}

// 4x4行列の積
Matrix4x4 Multiply(const Matrix4x4& m1, const Matrix4x4& m2) {
	Matrix4x4 result;
	for (int i = 0; i < 4; i++) {
		for (int j = 0; j < 4; j++) {
			result.m[i][j] = 0;
			for (int k = 0; k < 4; k++) {
				result.m[i][j] += m1.m[i][k] * m2.m[k][j];
			}
		}
	}
	return result;
}

// 逆行列
